import math

def addition(a,b):
    return a + b

def soustraction(a,b):
    return a - b

def multipliactionn(a,b):
    return a * b

def division(a,b):
    if (b != 0):
        return (a/b)

def cosinus(a):
    return math.cos(a)

def sinus(a):
    return math.sin(a)

def tangente(a):
    return math.tan(a)

def cotangente(a):
    return (math.cos(a)/math.sin(a))

def exponentielle(a):
    return math.exp(a)

def logarithme(a):
    return math.log10(a)

def logarithme_neperien(a):
    return math.log2(a)

def puissance_de_dix(a):
    return math.pow(10,a)
